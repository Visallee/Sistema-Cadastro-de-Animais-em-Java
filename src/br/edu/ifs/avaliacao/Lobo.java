package br.edu.ifs.avaliacao;

public class Lobo {
	private String raca;
	private int tamanhoAlcateia;
	private double velocidadeCorrida;
	private boolean capacidadeUivo;
	public String getRaca() {
		return raca;
	}
	public void setRaca(String raca) {
		this.raca = raca;
	}
	public int getTamanhoAlcateia() {
		return tamanhoAlcateia;
	}
	public void setTamanhoAlcateia(int tamanhoAlcateia) {
		this.tamanhoAlcateia = tamanhoAlcateia;
	}
	public double getVelocidadeCorrida() {
		return velocidadeCorrida;
	}
	public void setVelocidadeCorrida(double velocidadeCorrida) {
		this.velocidadeCorrida = velocidadeCorrida;
	}
	public boolean isCapacidadeUivo() {
		return capacidadeUivo;
	}
	public void setCapacidadeUivo(boolean capacidadeUivo) {
		this.capacidadeUivo = capacidadeUivo;
	}
}
