package br.edu.ifs.avaliacao;

public class Tartaruga {
	private String tipoCarapaca;
	private String habitatNatural;
	private int expectativaVida;
	private int tempoSubmerso;
	public String getTipoCarapaca() {
		return tipoCarapaca;
	}
	public void setTipoCarapaca(String tipoCarapaca) {
		this.tipoCarapaca = tipoCarapaca;
	}
	public String getHabitatNatural() {
		return habitatNatural;
	}
	public void setHabitatNatural(String habitatNatural) {
		this.habitatNatural = habitatNatural;
	}
	public int getExpectativaVida() {
		return expectativaVida;
	}
	public void setExpectativaVida(int expectativaVida) {
		this.expectativaVida = expectativaVida;
	}
	public int getTempoSubmerso() {
		return tempoSubmerso;
	}
	public void setTempoSubmerso(int tempoSubmerso) {
		this.tempoSubmerso = tempoSubmerso;
	}
}
