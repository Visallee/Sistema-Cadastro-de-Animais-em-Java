package br.edu.ifs.avaliacao;

public class Animal {
	private String nomeAnimal;
	private int idadeAnimal;
	private String especieAnimal;
	private double pesoAnimal;
	private String generoAnimal;
	public String getNomeAnimal() {
		return nomeAnimal;
	}
	public void setNomeAnimal(String nomeAnimal) {
		this.nomeAnimal = nomeAnimal;
	}
	public int getIdadeAnimal() {
		return idadeAnimal;
	}
	public void setIdadeAnimal(int idadeAnimal) {
		this.idadeAnimal = idadeAnimal;
	}
	public String getEspecieAnimal() {
		return especieAnimal;
	}
	public void setEspecieAnimal(String especieAnimal) {
		this.especieAnimal = especieAnimal;
	}
	public double getPesoAnimal() {
		return pesoAnimal;
	}
	public void setPesoAnimal(double pesoAnimal) {
		this.pesoAnimal = pesoAnimal;
	}
	public String getGeneroAnimel() {
		return generoAnimal;
	}
	public void setGeneroAnimel(String generoAnimal) {
		this.generoAnimal = generoAnimal;
	}

}
